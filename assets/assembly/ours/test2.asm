SECTION TEXT
    S_INPUT BUFFER, 100
    S_OUTPUT BUFFER, 100

    INPUT N1
    INPUT N2
    LOAD N1
    SUB N2
    STORE N1
    OUTPUT N1

    INPUT N1
    LOAD N1
L1:  
    SUB UM
    STORE N1
    OUTPUT N1
    JMPP L1
    JMPZ CASE
FIM:
    STOP

CASE: ADD UM
    STORE N1
    OUTPUT N1
    SUB UM
    JMPZ FIM

SECTION DATA
BUFFER: SPACE 20
N1:    SPACE 1
N2:    SPACE 1
ZERO:   CONST 0
UM:     CONST 1